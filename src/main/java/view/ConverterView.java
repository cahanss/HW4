package view;

import controller.Facade;
import java.io.Serializable;
import javax.ejb.EJB;
import javax.enterprise.context.Conversation;
import javax.enterprise.context.ConversationScoped;
import javax.inject.Inject;
import javax.inject.Named;



@Named("converterView")
@ConversationScoped
public class ConverterView implements Serializable {
    @EJB
    private Facade facade;
    private double conversionAmount;
    private String convertFrom;
    private String convertTo;
    private double convertedAmount = 0.0;
    private Exception transactionFailure;
    @Inject
    private Conversation conversation;

    private void startConversation() {
        if (conversation.isTransient()) {
            conversation.begin();
        }
    }

    private void stopConversation() {
        if (!conversation.isTransient()) {
            conversation.end();
        }
    }

    private void handleException(Exception e) {
        stopConversation();
        e.printStackTrace(System.err);
        transactionFailure = e;
    }
   

    

    public void convert() {
        try {
            startConversation();
            transactionFailure = null;
            convertedAmount = facade.convert(conversionAmount, convertFrom, convertTo);
        } catch (Exception e) {
            handleException(e);

        }
    }

    public double getConversionAmount() {
        return conversionAmount;
    }

    public void setConversionAmount(double conversionAmount) {
        this.conversionAmount = conversionAmount;
    }

    public String getConvertFrom() {
        return convertFrom;
    }

    public void setConvertFrom(String convertFrom) {
        this.convertFrom = convertFrom;
    }

    public String getConvertTo() {
        return convertTo;
    }

    public void setConvertTo(String convertTo) {
        this.convertTo = convertTo;
    }

    public double getConvertedAmount() {
        return convertedAmount;
    }

    public void setConvertedAmount(double convertedAmount) {
        this.convertedAmount = convertedAmount;
    }
    

    
    
}